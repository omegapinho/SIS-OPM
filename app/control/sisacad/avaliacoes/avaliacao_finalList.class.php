    /**
     * Inline record editing
     * @param $param Array containing:
     *              key: object ID value
     *              field name: object attribute to be updated
     *              value: new attribute content 
     */
    public function onInlineEdit($param)
    {
        try
        {
            // get the parameter $key
            $field = $param['field'];
            $key   = $param['key'];
            $value = $param['value'];
            
            TTransaction::open('sisacad'); // open a transaction with database
            $object = new avaliacao_final($key); // instantiates the Active Record
            $object->{$field} = $value;
            $object->store(); // update the object in the database
            TTransaction::close(); // close the transaction
            
            $this->onReload($param); // reload the listing
            new TMessage('info', "Record Updated");
        }
        catch (Exception $e) // in case of exception
        {
            new TMessage('error', '<b>Error</b> ' . $e->getMessage()); // shows the exception error message
            TTransaction::rollback(); // undo all pending operations
        }
    }
    
    /**
     * Register the filter in the session
     */
    public function onSearch()
    {
        // get the search form data
        $data = $this->form->getData();
        
        // clear session filters
        TSession::setValue('avaliacao_finalList_filter_materia_id',   NULL);
        TSession::setValue('avaliacao_finalList_filter_data_fim',   NULL);
        TSession::setValue('avaliacao_finalList_filter_oculto',   NULL);

        if (isset($data->materia_id) AND ($data->materia_id)) {
            $filter = new TFilter('materia_id', '=', "$data->materia_id"); // create the filter
            TSession::setValue('avaliacao_finalList_filter_materia_id',   $filter); // stores the filter in the session
        }


        if (isset($data->data_fim) AND ($data->data_fim)) {
            $filter = new TFilter('data_fim', '=', "$data->data_fim"); // create the filter
            TSession::setValue('avaliacao_finalList_filter_data_fim',   $filter); // stores the filter in the session
        }


        if (isset($data->oculto) AND ($data->oculto)) {
            $filter = new TFilter('oculto', '=', "$data->oculto"); // create the filter
            TSession::setValue('avaliacao_finalList_filter_oculto',   $filter); // stores the filter in the session
        }

        
        // fill the form with data again
        $this->form->setData($data);
        
        // keep the search data in the session
        TSession::setValue('avaliacao_final_filter_data', $data);
        
        $param=array();
        $param['offset']    =0;
        $param['first_page']=1;
        $this->onReload($param);
    }
    
    /**
     * Load the datagrid with data
     */
    public function onReload($param = NULL)
    {
        try
        {
            // open a transaction with database 'sisacad'
            TTransaction::open('sisacad');
            
            // creates a repository for avaliacao_final
            $repository = new TRepository('avaliacao_final');
            $limit = 10;
            // creates a criteria
            $criteria = new TCriteria;
            
            // default order
            if (empty($param['order']))
            {
                $param['order'] = 'id';
                $param['direction'] = 'asc';
            }
            $criteria->setProperties($param); // order, offset
            $criteria->setProperty('limit', $limit);
            

            if (TSession::getValue('avaliacao_finalList_filter_materia_id')) {
                $criteria->add(TSession::getValue('avaliacao_finalList_filter_materia_id')); // add the session filter
            }


            if (TSession::getValue('avaliacao_finalList_filter_data_fim')) {
                $criteria->add(TSession::getValue('avaliacao_finalList_filter_data_fim')); // add the session filter
            }


            if (TSession::getValue('avaliacao_finalList_filter_oculto')) {
                $criteria->add(TSession::getValue('avaliacao_finalList_filter_oculto')); // add the session filter
            }

            
            // load the objects according to criteria
            $objects = $repository->load($criteria, FALSE);
            
            if (is_callable($this->transformCallback))
            {
                call_user_func($this->transformCallback, $objects, $param);
            }
            
            $this->datagrid->clear();
            if ($objects)
            {
                // iterate the collection of active records
                foreach ($objects as $object)
                {
                    // add the object inside the datagrid
                    $object->turma = $object->avaliacao_turma->turma->nome;
                    $this->datagrid->addItem($object);
                }
            }
            
            // reset the criteria for record count
            $criteria->resetProperties();
            $count= $repository->count($criteria);
            
            $this->pageNavigation->setCount($count); // count of records
            $this->pageNavigation->setProperties($param); // order, page
            $this->pageNavigation->setLimit($limit); // limit
            
            // close the transaction
            TTransaction::close();
            $this->loaded = true;
        }
        catch (Exception $e) // in case of exception
        {
            // shows the exception error message
            new TMessage('error', $e->getMessage());
            // undo all pending operations
            TTransaction::rollback();
        }
    }
    
